def correct_site(site: dict) -> dict:
    code = site["code"]
    original_lng = site["lng"]
    original_lat = site["lat"]

    # 优先级 1: geocode_results2（人工校验过的）
    if code in geo2_by_code:
        g = geo2_by_code[code]
        site["lng"] = g["lng"]
        site["lat"] = g["lat"]
        site["province"] = g["province"]
        site["city"] = g["city"]
        return site

    # 优先级 2: MANUAL_FIXES
    if code in MANUAL_FIXES:
        fix = MANUAL_FIXES[code]
        site["lng"] = fix["lng"]
        site["lat"] = fix["lat"]
        site["province"] = fix["province"]
        site["city"] = fix["city"]
        return site

    # 优先级 3: 南沙统一修正（7个共享错误坐标的网点）
    if abs(original_lng - 113.525178) < 0.001 and abs(original_lat - 22.801435) < 0.001:
        fix = MANUAL_FIXES["南沙一期/二期/三期/四期_统一修正"]
        site["lng"] = fix["lng"]
        site["lat"] = fix["lat"]
        site["province"] = fix["province"]
        site["city"] = fix["city"]
        return site

    # 优先级 4: ADDRESS_MAP → geocode_results
    if code in ADDRESS_MAP:
        addr = ADDRESS_MAP[code]
        if addr in geo1:
            g = geo1[addr]
            site["lng"] = g["lng"]
            site["lat"] = g["lat"]
            site["province"] = g["province"]
            site["city"] = g["city"]
            return site

    return site


